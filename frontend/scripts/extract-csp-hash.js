import { readFileSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const htmlPath = resolve(__dirname, "..", "build", "index.html");
const outPath = resolve(__dirname, "..", "..", "web", "csp_hash.go");

const html = readFileSync(htmlPath, "utf-8");
const match = html.match(/<script>([\s\S]*?)<\/script>/);
if (!match) {
	console.error("No inline <script> found in index.html");
	process.exit(1);
}

const hash = createHash("sha256").update(match[1]).digest("base64");

writeFileSync(
	outPath,
	`// Code generated by frontend/scripts/extract-csp-hash.js. DO NOT EDIT.

package web

const ScriptHash = "'sha256-${hash}'"
`,
);

console.log("CSP hash:", hash);
